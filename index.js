function add(x , y) {
  return (x + y) ;
}

function subtract(x , y) {
  return (x - y) ;
}

function multiply(x , y) {
  return (x * y) ;
}

function divide(x , y) {
  return (x / y) ;
}

function inc(n) {
  return n += 1 ;
}

function dec(n) {
  return n -= 1 ;
}

function makeInt(n) {
  n = "2.3" ;
  return parseInt('n' , 10) ;
}

function preserveDecimal(n) {
  n = "2.3" ;
  return parseFloat('2.3') ;
}